    protected void AssetAccept()
    {
        try
        {
            CCommon objConn = new CCommon();
            SqlConnection sqlcon = new SqlConnection(objConn.AppConnectionString);

            SqlCommand sqlcmd = new SqlCommand();
            sqlcmd.Connection = sqlcon;
            sqlcmd.CommandType = CommandType.StoredProcedure;
            sqlcmd.CommandText = "Asset_Accept_SP";
            sqlcmd.CommandTimeout = 0;

            SqlParameter TransactionID = new SqlParameter();
            TransactionID.SqlDbType = SqlDbType.VarChar;
            TransactionID.Value = hdnAssetID.Value; //GVDetailsForAssetApprover.Rows[0].Cells[7].Text.Trim(); //change on 29/01/24
            TransactionID.ParameterName = "@TransRefNo";
            sqlcmd.Parameters.Add(TransactionID);

            SqlParameter UserID = new SqlParameter();
            UserID.SqlDbType = SqlDbType.VarChar;
            UserID.Value = Session["UserId"].ToString();
            UserID.ParameterName = "@UserID";
            sqlcmd.Parameters.Add(UserID);

            sqlcon.Open();
            int i = sqlcmd.ExecuteNonQuery();
            sqlcon.Close();

            if (i > 0)
            {
                ViewState["Result"] = "Yes";
                lblMsgXls.Visible = true;
                lblMsgXls.Text = "Accepted Successfully :";
            }
            else
            {
                return;
            }
        }

        catch (Exception ex)
        {
            lblMsgXls.Text = ex.Message;
        }
    }

